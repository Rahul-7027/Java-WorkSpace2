package com.company;

public class Practice_13 {
    public static void main(String[] args) {
        int [] marks = {45, 67, 63, 99, 100};
        System.out.println("Reverse Order Array printing : ");
        for (int i = marks.length-1; i >=0 ; i--) {
            System.out.println(marks[i]);
        }

    }
}
